                    <h2><i class="fa fa-bank"></i> Tax on company cars</h2>
                    <div class="frame">
                    <h3><i class="fa fa-edit"></i> You’ll pay tax if a company car is given to you or your family to use privately, including for commuting.</h3>
                    <div class="frame_note">
                        <p>You pay tax on the value of the company car, which depends on things like how much it would cost to buy and the type of fuel it uses.</p>
                        <p>Its value is reduced if you have the car part-time or you pay something towards its cost.</p>
                        <br />
                        <p>You can check or update your company car tax online: <a href="https://www.gov.uk/update-company-car-details">here</a>.</p>
