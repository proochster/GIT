			        <a name="whats_not_included"></a>				
                    <h2><i class="fa fa-list-alt"></i> Company car BIK rates 2015 - 2020</h2>
                    <div class="frame">
                        <h3><i class="fa fa-edit"></i> The rates shown are for April 2015 to March 2020 as confirmed in Budget 2015.</h3>
                        <div class="frame_note">
                            <p>Company car BIK rates in 2015 start at 13% for conventional petrol and 16% for diesel vehicles, the rate increasing in 1% increments with increasing CO2 up to a maximum of 37%.</p>
                            <br />
                            <p>The rates for diesel vehicles are generally 3% higher than those for petrol equivalents, this is to take into account the greater emissions of local pollutants such as NOx. However, the 3% diesel differential will be scrapped from April 2016.</p>
                            <br />
                            <p>All hybrid company cars also receive a reduced BIK rate as a result of their lower CO2 emissions, which tends to reduce their BIK rates by at least 5%. Note that for diesel hybrid powered cars, the 3% diesel surcharge does not apply.</p>
                            <br />
                            <p>Ultra Low Carbon Vehicles (ULEVs) are rewarded with lower BIK rates – from April 2015, plug-in hybrids are rated at 5% or 9% (depending on official CO2 emissions), and battery electric cars are rated at 5%.</p>
                            <br />
                            <p>In April 2016 and 2017, BIK rates for cars in the 0-50 g/km and 51-75 g/km CO2 bands are due to increase by 2% per year with a 3%-4% rise planned for 2018. However, a 4% differential between the three lowest CO2 bands will be maintained in 2017, reducing to a 3% differential in 2018 and 2% thereafter in line with the previous Budget announcements.</p>
                            <br />
                            <p>The Government will review the incentives for ULEVs in light of market developments in Budget 2016, to inform decisions on Company Car Tax from 2020-21 onwards.</p>
                            <br />
                            <p>The tables below show how the percentage BIK rates vary with vehicle CO2. The table represents both petrol, diesel and electric related BIK rates for this and the next four tax years.</p>
                            <br />
                            <p>For vehicles registered before 1st January 1998, there is no reliable source of CO2 emissions data. If such vehicles are used as company cars, they are taxed according to engine size: Up to 1400cc, BIK 15%; 1401-2000cc, BIK 22%; Over 2000cc, BIK 32%.</p>
                            <br />
